                <h1>
                    <span>PEDOMAN</span> TATA KELOLA PERUSAHAAN
                </h1>
            </div>
            <blockquote>
                Parameter dan pedoman yang diterapkan oleh ADHI dengan mengambil praktik-praktik bisnis terbaik yang disesuaikan dengan budaya
                Indonesia serta budaya di negara di mana ADHI beroperasi.
            </blockquote>
            <p style="margin: 1px 0px 25px; padding: 0px; border: 0px; outline: 0px; vertical-align: baseline; line-height: 25px; font-family: robotolight; font-size: 15px; text-align: justify; background: transparent;">
                <span style="margin: 0px; padding: 0px; border: 0px; outline: 0px; vertical-align: baseline; color: rgb(254, 0, 0); background: transparent;">
                    KEBIJAKAN GCG</span>
                <br />ADHI berupaya memberikan nilai tambah kepada para pemangku kepentingan melalui penerapan prinsip tata kelola perusahaan
                yang baik (
                <em>Good Corporate Governance</em>) secara konsisten dan konsekuen, dengan menjadikannya sebagai budaya kerja yang berlaku di
                dalam Perusahaan. Pemahaman ini mendasari kebijakan ADHI untuk melaksanakan tata kelola yang baik dalam setiap kegiatan
                bisnisnya untuk mencapai tujuan bisnis jangka panjang yang berkesinambungan. Pelaksanaan GCG ADHI mengacu pada Peraturan
                Menteri Negara BUMN No. PER-01/MBU/2011 tanggal 1 Agustus 2011 dan PER-09/MBU/2012 tanggal 6 Juli 2012. Selain itu prinsip tata
                kelola perusahan ADHI mengacu pada parameter GCG yang dikeluarkan oleh 
                <em>Organization for Economic Cooperation and Development</em> (OECD) dan Komite Nasional GCG (KNGCG) serta mengambil
                praktik-praktik bisnis terbaik yang disesuaikan dengan budaya Indonesia serta budaya di negara di mana ADHI beroperasi</p>
            <p style="margin: 1px 0px 5px; padding: 0px; border: 0px; outline: 0px; vertical-align: baseline; line-height: 25px; font-family: robotolight; font-size: 15px; text-align: justify; background: transparent;">
                ADHI menerapkan prinsip-prinsip GCG pada setiap aspek bisnis dan pada semua jajaran organisasi, yang diwujudkan dalam
                aspek-aspek seperti:</p>
            <ul>
                <li style="text-align: justify;">Pelaksanaan tugas dan tanggung jawab anggota Dewan Komisaris dan Direksi.</li>
                <li style="text-align: justify;">Penerapan fungsi kepatuhan</li>
                <li style="text-align: justify;">Pengelolaan manajemen risiko</li>
                <li style="text-align: justify;">Pelaksanaan transparansi keuangan dan non-keuangan</li>
                <li style="text-align: justify;">Pelaksanaan tugas-tugas komite- komite dan satuan kerja yang melaksanakan fungsi
                    pengendalian internal.</li>
            </ul>
            <p style="margin: 1px 0px 5px; padding: 0px; border: 0px; outline: 0px; vertical-align: baseline; line-height: 25px; font-family: robotolight; font-size: 15px; text-align: justify; background: transparent;">
                Sejalan dengan rekomendasi OECD, prinsip-prinsip GCG di ADHI meliputi lima elemen kunci yang disingkat ”TARIF” sebagai
                berikut:</p>
            <p style="margin: 1px 0px 5px; padding: 0px; border: 0px; outline: 0px; vertical-align: baseline; line-height: 25px; font-family: robotolight; font-size: 15px; text-align: justify; background: transparent;">
            </p>
            <ul>
                <li style="text-align: justify;">“T” (
                    <em>Transparency</em>/Transparansi) adalah keterbukaan dalam pelaksanaan proses pengambilan keputusan dan keterbukaan dalam
                    menyebarkan informasi penting yang relevan mengenai perusahaan.</li>
                <li style="text-align: justify;">“A” (
                    <em>Accountability</em>/Akuntabilitas), adalah kejelasan fungsi, operasional dan pertanggungjawaban dari organisasi yang
                    memungkinkan pengelolaan Perusahaan yang efektif.</li>
                <li style="text-align: justify;">“R” (
                    <em>Responsibility</em>/Pertanggungjawaban), adalah penyelarasan pengelolaan Perusahaan kepatuhan terhadap ketentuan dan
                    perundang-undangan yang berlaku dan prinsip-prinsip tentang pengelolaan yang baik.</li>
                <li style="text-align: justify;">“I” (
                    <em>Independency</em>/Kemandirian), pengelolaan perusahaan yang profesional tanpa kepentingan atau pengaruh dari pihak
                    manapun yang tidak sesuai dengan peraturan dan perundang-undangan dan prinsip-prinsip korporasi yang baik.</li>
                <li style="text-align: justify;">“F” (
                    <em>Fairness</em>/Kewajaran), adalah perlakuan yang adil dan sama terhadap seluruh pemangku kepentingan dalam memenuhi
                    hak-hak mereka, berdasarkan ketentuan dan perundang-undangan yang berlaku.</li>
            </ul>
            <p style="text-align: justify;"></p>
            <p style="margin: 1px 0px 25px; padding: 0px; border: 0px; outline: 0px; vertical-align: baseline; line-height: 25px; font-family: robotolight; font-size: 15px; text-align: justify; background: transparent;">
                <span style="margin: 0px; padding: 0px; border: 0px; outline: 0px; vertical-align: baseline; color: rgb(254, 0, 0); background: transparent;">
                    ORGAN TATA KELOLA PERUSAHAAN</span>
                <br />Tata kelola perusahaan yang baik di lingkungan ADHI diimplementasikan melalui organ tata kelola yaitu: Pemegang Saham,
                Dewan Komisaris, Direksi, Komite-komite di bawah Dewan Komisaris dan Komite-komite di bawah Direksi, serta jajaran manajemen
                yang terdiri atas eksekutif dan manajer senior, Sekretaris Perusahaan dan Satuan Pengawas Internal.</p>
